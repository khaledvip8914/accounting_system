async function main() {
  console.log('Updating all account descriptions...');
  let updated = 0;

  for (const [code, desc] of Object.entries(descriptions)) {
    const res = await prisma.account.updateMany({
      where: { code, OR: [{ description: null }, { description: '' }] },
      data: { description: desc }
    });
    if (res.count > 0) {
      console.log(`Updated description for code ${code}`);
      updated += res.count;
    }
  }

  console.log(`Finished updating ${updated} account descriptions!`);
}

main().finally(() => prisma.$disconnect());
